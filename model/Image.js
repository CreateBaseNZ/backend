/*=========================================================================================
REQUIRED MODULES
=========================================================================================*/

const mongoose = require("mongoose");

/*=========================================================================================
VARIABLES
=========================================================================================*/

const Schema = mongoose.Schema;

/*=========================================================================================
CREATE IMAGES MODEL
=========================================================================================*/

const ImageSchema = new Schema({
  file: {
    id: {
      type: Schema.Types.ObjectId
    },
    name: {
      type: String
    }
  }
});

/*=========================================================================================
MIDDLEWARE
=========================================================================================*/

/*=========================================================================================
STATIC
=========================================================================================*/

/*=========================================================================================
METHOD
=========================================================================================*/

/*=========================================================================================
EXPORT ACCOUNT MODEL
=========================================================================================*/

module.exports = Image = mongoose.model("images", ImageSchema);

/*=========================================================================================
END
=========================================================================================*/
