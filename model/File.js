/*=========================================================================================
REQUIRED MODULES
=========================================================================================*/

const mongoose = require("mongoose");

/*=========================================================================================
VARIABLES
=========================================================================================*/

const Schema = mongoose.Schema;

/*=========================================================================================
CREATE COMMENT MODEL
=========================================================================================*/

const FileSchema = new Schema({
  _id: {
    type: Schema.Types.ObjectId
  },
  length: {
    type: Number
  },
  chunkSize: {
    type: Number
  },
  uploadDate: {
    type: Date
  },
  filename: {
    type: String
  },
  md5: {
    type: String
  },
  contentType: {
    type: String
  },
  metadata: {
    type: Schema.Types.Mixed
  }
});

/*=========================================================================================
EXPORT COMMENT MODEL
=========================================================================================*/

module.exports = Comment = mongoose.model("fs.file", FileSchema);

/*=========================================================================================
END
=========================================================================================*/
